// 小説の内容をレンダリングする関数
function renderChapter() {
    const novelContent = document.querySelector('.novel-content');
    novelContent.innerHTML = ''; // コンテナをクリア
    const currentChapterContent = chapters[currentChapter - 1];
    currentChapterContent.forEach(paragraph => {
        const p = document.createElement('p');
        p.textContent = paragraph; // テンプレートリテラルを使用して段落を設定
        novelContent.appendChild(p);
    });
    document.getElementById('chapter-info').textContent = `章 ${currentChapter}`;

    // 最初の章では「前の章」ボタンを非表示にする
    const prevButton = document.querySelector('button[onclick="prevChapter()"]');
    if (currentChapter === 1) {
        prevButton.style.display = 'none';
    } else {
        prevButton.style.display = 'inline';
    }
}

// 前の章に移動する関数
function prevChapter() {
    if (currentChapter > 1) {
        currentChapter--;
        renderChapter();
    }
}

// 次の章に移動する関数
function nextChapter() {
    if (currentChapter < chapters.length) {
        currentChapter++;
        renderChapter();
    }
}

// 初期表示
renderChapter();